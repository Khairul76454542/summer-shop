function card1() {
  const card1Price = document.getElementById("card-1").innerText;
  const totalPriceElement = document.getElementById("total-price");
  const totalPrice = parseFloat(totalPriceElement.innerText);
  const total = parseFloat(card1Price) + totalPrice;
  totalPriceElement.innerText = total;
  document.getElementById("final").innerText = total;

  document.getElementById("final").innerText = total;
  const cardTitle = document.getElementById("title-1").innerText;
  const list = document.createElement("li");
  list.innerHTML = cardTitle;
  document.getElementById("list").appendChild(list);
}
